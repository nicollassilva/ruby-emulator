import java.util.concurrent.TimeUnit;


public class WiredCustomFreezes extends WiredActionItem {

    public WiredCustomFreezes(RoomItemData itemData, Room room) {
        super(itemData, room);
    }

    @Override
    public boolean requiresPlayer() {
        return true;
    }

    @Override
    public int getInterface() {
        return 0;
    }

    @Override
    public void onEventComplete(WiredItemEvent event) {
        if (!(event.entity instanceof PlayerEntity)) {
            return;
        }

        PlayerEntity playerEntity = ((PlayerEntity) event.entity);

        if (playerEntity.getPlayer() == null || playerEntity.getPlayer().getSession() == null) {
            return;
        }

        for (long itemId : this.getWiredData().getSelectedIds()) {
            final RoomItemFloor floorItem = this.getRoom().getItems().getFloorItem(itemId);

            if (floorItem == null || floorItem instanceof WiredFloorItem)
                continue;

            freezes(playerEntity, floorItem);
            CometThreadManager.getInstance().executeSchedule(() -> freezes(playerEntity, floorItem), this.getWiredData().getDelay() * 500, TimeUnit.MILLISECONDS);
        }
    }

    private void freezes(PlayerEntity playerEntity, RoomItemFloor floorItem) {
        playerEntity.cancelWalk();
        playerEntity.setCanWalk(false);
        playerEntity.setIsFreeze(true);
    }
}
